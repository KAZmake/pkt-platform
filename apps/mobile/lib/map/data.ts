import type { Feature, Point } from 'geojson';

export interface FarmProperties {
  id: string;
  name: string;
  owner: string;
  area: number;
  crops: string[];
  district: string;
  hasActiveLoan: boolean;
  loanAmount?: number;
  phone?: string;
}

export type FarmFeature = Feature<Point, FarmProperties>;

function farm(
  id: string,
  name: string,
  owner: string,
  area: number,
  crops: string[],
  district: string,
  lat: number,
  lng: number,
  hasActiveLoan: boolean,
  loanAmount?: number,
): FarmFeature {
  return {
    type: 'Feature',
    geometry: { type: 'Point', coordinates: [lng, lat] },
    properties: { id, name, owner, area, crops, district, hasActiveLoan, loanAmount },
  };
}

